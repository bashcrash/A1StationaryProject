    private void saveButtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtActionPerformed
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        int cusid;
        try {

            rs.moveToInsertRow();
            rs.updateInt(1, Integer.parseInt(Custid.getText()));
            rs.updateString(2, firstname.getText());
            rs.updateString(3, lastname.getText());
            rs.updateString(4, format.format(jDateChooser1.getDate()));
            rs.updateString(5, phone.getText());
            rs.updateString(6, address.getText());
            rs.updateString(7, email.getText());
            rs.insertRow();
            rs.last();

            Custid.setText(String.valueOf(getNextcustID()));
            firstname.setText("");
            lastname.setText("");
            jDateChooser1.setDate(null);
            phone.setText("");
            address.setText("");
            email.setText("");

            JOptionPane.showMessageDialog(null, "Record added successfully",
                    "information", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
//            if(rs.equals(Custid))
//            {
            JOptionPane.showMessageDialog(null, ex);
            JOptionPane.showMessageDialog(null, "Customer Exists", "Information", JOptionPane.INFORMATION_MESSAGE);
//            }
        }         // TODO add your handling code here:
    }//GEN-LAST:event_saveButtActionPerformed

    private void butViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butViewActionPerformed
        try {
            rs = statement.executeQuery("Select * From customer WHERE cuslastname='" + lastname.getText() + "'"
                    + "AND cusname='" + firstname.getText() + "'");
            rs.first();

            Custid.setText(String.valueOf(rs.getInt(1)));
            jDateChooser1.setDate(rs.getDate(4));
            phone.setText(rs.getString(5));
            address.setText(rs.getString(6));
            email.setText(rs.getString(7));
            

        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }


    
    }//GEN-LAST:event_butViewActionPerformed

