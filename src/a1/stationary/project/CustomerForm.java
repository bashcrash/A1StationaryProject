    public static int getNextcustID() {
        int curCutid;
        try {
            rs.last();
            curCutid = rs.getInt("customerID");
            int intNextCutId = curCutid + 1;
            return intNextCutId; //String.format("%01d", intNextembId);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
            JOptionPane.showMessageDialog(null, "Error on last record",
                    "information", JOptionPane.INFORMATION_MESSAGE);
            return 1;
        }
    }

